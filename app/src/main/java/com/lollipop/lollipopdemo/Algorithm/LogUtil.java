package com.lollipop.lollipopdemo.Algorithm;

public class LogUtil {
    static void log(Object[] array) {
        for (Object i1 : array) {
            System.out.println(i1);
        }
    }

    static void log(int[] array) {
        for (int i1 : array) {
            System.out.println(i1);
        }
    }
}
